                                <p>
									Androgenetic alopecia is one of the most common cause of hair loss in men. This is also commonly called as the Male Pattern Baldness. The Androgentic alopecia usually starts manifesting itself in the third decade of life and as the age progresses, the grade of baldness increases every decade. However, in some people it starts in age as low as 20 years.
									</p>
									
