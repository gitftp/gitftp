                                <div class="dept-subtitle-tabs">Hair Loss In Men</div>
                                <p>There are several reasons that account for hair loss and surprisingly they vary in men and women. Various studies have revealed that at an average a man loses 100-150 hairs per day, which is quite normal as they tend to grow back following three phases: anagen, catagen and telogen. However, any lapse in regrowth pattern could result in what we term as hair loss. It has been observed that every man experiences some degree of hair loss in his lifetime, which may tend to increase with age. The hair loss reaches up to 12% in late 20’s and tends to increase up to 50% in late 50’s.</p>
								<p>
								What exaggerates the condition is the psychological effect of hair loss. While some people face the fact that they are balding sportingly, others restrict themselves socially. Gratefully, one could escape the dilemma by seeking help from excellent treatments that act proactively in preventing any further male hair loss. Hair transplant procedures like FUT (Follicular Unit Transplantation) and FUE (Follicular Unit Extraction) have managed to adhere by the transplant standards and predict results that are meant forever.
								</p>
