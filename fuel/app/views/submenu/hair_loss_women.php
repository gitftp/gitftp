                                <div class="dept-subtitle-tabs">Female Pattern Baldness</div>
                                <p>In females, baldness takes place when hair starts falling out and new hair does not grow in its place. The initial symptoms start with the thinning of the scalp hair. Hair thinning starts mainly on the top and crown area of the head, although the front hairline remains as it is. It is very rare that women face complete baldness like men.
									</p>
									<p>
									The Ludwig Classification segregates female pattern baldness (androgenetic alopecia) in to 3 stages. Referred as Ludwig Scale, these stages help in understanding hair loss according to the affected area and severity. The Ludwig Scale is extremely helpful to hair surgeons in the diagnosing the degree of hair already lost, estimated hair loss in future and the best treatment to stop by.
									</p>
