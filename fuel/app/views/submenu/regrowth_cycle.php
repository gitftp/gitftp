                                <div class="dept-title-tabs">Regrowth Cycle</div>
                                <img alt="" class="img-responsive" src="images/dept-dymmy.jpg" />
                                <div class="dept-subtitle-tabs">Regrowth Cycle</div>
                                <p>By the time a foetus is 22 weeks old, the hair follicles start forming and within the next few weeks, the unborn baby has the makings of a full head of hair. As a matter of fact, at around this time, there are close to 5 million hair follicles all over the baby’s body, out of which, one million are on the head alone. This is actually the real number of hair follicles that one will have, because during the course of a person’s life, there are no new follicles that are created. The reason why the density of scalp hair reduces over time is because the scalp expands with age!</p>
								<p>
								The actual hair or the hair follicles consists of two parts – the follicle and the shaft – the follicle is the part that thrives inside the skin, and the shaft is the portion that is visible right above the scalp. The follicles are almost like tunnels of the epidermis, and this tunnel actually goes right into the dermis. There are multiple parts to the follicle and each part has a distinctive role to play. The base is called the papilla and this is the part that contains the capillaries which provide nourishment to the cells. The part around the papilla is known as the bulb and this is the living part of the follicle. The cells within the bulb divide every 24 to 72 hours, which is in fact much faster than any of the other cells in the body.
								</p>
								<p>
								There are two sheaths that encapsulate the follicle, the inner and the outer ones, and these are these sheaths that protect the hair shaft. The inner sheath ends right below where the oil gland is, while the outer sheath continues all the way up to the actual gland. There is a muscle called the erector pili muscle which attaches itself right below the gland. It is the contraction of this muscle that makes the hair stand up and also secrete oil. 
								</p>
								<p>
								The hair actually goes through three main stages of growth – Anagen, Catagen and Telogen. Each hair undergoes through the same three phases and once the cycle has been completed, the cycle starts all over again. And each time the cycle restarts, a new stand of hair starts to form.
								</P>
                            </div>

                            <div class="tab-pane fade" id="b">
                                <div class="dept-title-tabs">Anagen</div>
                                <img alt="" class="img-responsive" src="images/dept-page-02.jpg" />
                                <div class="dept-subtitle-tabs">Anagen</div>
                                <p>Anagen: This is the growth phase and starts with the papilla. The length of this phase can extend from two years and go on till about six years. The lifespan of this stage is mainly decided by genetics and the longer the hair stays in this phase, the longer it will grow. The cells within the papilla split to create new hair fibres. The follicle entombs itself within the dermal layers of the skin and hence receives ample nourishment. .
									</p>
									
                            </div>

                            <div class="tab-pane fade" id="c">
                                <div class="dept-title-tabs">Catagen</div>
                                <img alt="" class="img-responsive" src="images/dept-page-03.jpg" />
                                <div class="dept-subtitle-tabs">Catagen</div>
                                
								<p>Catagen: The body sends out signals, which declare the end of the anagen phase and the start of the catagen phase. Also known as the transitional phase, this is the time when the follicle gets a chance to renew itself. Normally, this phase lasts for about two weeks and the hair follicles shrinks a little. The shaft is cut off and this necessitates the hair shaft to be pushed up. 
								</p>



                            </div>

                            <div class="tab-pane fade" id="d">
                                <div class="dept-title-tabs">Telogen</div>
                                <img alt="" class="img-responsive" src="images/dept-page-04.jpg" />
                                <div class="dept-subtitle-tabs">Telogen</div>
                                <p>Telogen: This phase extends from one to four months and is also known as the resting phase. The epidermal cells that line the follicles grow normally and many a times, they get accumulated right around the place, where the hair forms. However, there will be a point in time when the follicles will start growing again and this will be the time when the hair will be pushed out. The hair will fall, creating space for the growth of a new hair. 
.</p>
