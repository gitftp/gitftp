                                <div class="dept-subtitle-tabs">Why does this occur? </div>
                                

                                <p>This is because of the effect of the male hormone derivative Dihydortestosterone on the hair roots located on the front and top of the scalp. However, the hair on the sides and the back are usually spared of this effect. This is because, the  male hormone does not act on the receptors of the hair roots located on the back side as they have arisen from different embryonic layer as that of the front and the top of the scalp.
									<p>
									</p>Over the period of years, the male hormone keeps on acting on the hair, they keep on thinning with every cycle and eventually they fall off. Depending on the degree of hair loss, they can be categorized in to different grades, namely Norwood Hamilton, II-VII.
