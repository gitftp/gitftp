								<li>
								The actual donor area and its size
								</li>
								<li>
								The structural similarity of the hair that is being harvested in comparison to the hair on the head
								</li>
								<li>
								The length of the body hair and how much it will have to be trimmed
								</li>
								<li>
								The actual number of follicular units which can be made available

								</li>
